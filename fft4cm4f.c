// Cortex-M4F version of djbfft for float with gcc style inline assembly
//
// Bottleneck in cortex-m is memory access, so most speed gains come from
// using vldmia/vstmia and vldr.64/vstr.64 instead of single loads.
// Using fused multiply-add could lead to more precision and slightly smaller
// code, but it takes 3 cycles to compute, opposed to 2 cycles for a seperate 
// vmul/vadd. On STM32F466, it runs slightly faster without fused multiply-add.  
// Choose by modifying USEFMA. 

#define USEFMA	0

